defmodule Server.Accounts do
  alias Server.Guardian
  import Comeonin.Bcrypt, only: [checkpw: 2, dummy_checkpw: 0]
  @moduledoc """
  The Accounts context.
  """

  import Ecto.Query, warn: false
  alias Server.Repo

  alias Server.Accounts.Patient

  @doc """
  Returns the list of patients.

  ## Examples

      iex> list_patients()
      [%Patient{}, ...]

  """
  def list_patients do
    Repo.all(Patient)
  end

  @doc """
  Gets a single patient.

  Raises `Ecto.NoResultsError` if the Patient does not exist.

  ## Examples

      iex> get_patient!(123)
      %Patient{}

      iex> get_patient!(456)
      ** (Ecto.NoResultsError)

  """
  def get_patient!(id), do: Repo.get!(Patient, id)

  @doc """
  Creates a patient.

  ## Examples

      iex> create_patient(%{field: value})
      {:ok, %Patient{}}

      iex> create_patient(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_patient(attrs \\ %{}) do
    %Patient{}
    |> Patient.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a patient.

  ## Examples

      iex> update_patient(patient, %{field: new_value})
      {:ok, %Patient{}}

      iex> update_patient(patient, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_patient(%Patient{} = patient, attrs) do
    patient
    |> Patient.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a patient.

  ## Examples

      iex> delete_patient(patient)
      {:ok, %Patient{}}

      iex> delete_patient(patient)
      {:error, %Ecto.Changeset{}}

  """
  def delete_patient(%Patient{} = patient) do
    Repo.delete(patient)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking patient changes.

  ## Examples

      iex> change_patient(patient)
      %Ecto.Changeset{data: %Patient{}}

  """
  def change_patient(%Patient{} = patient, attrs \\ %{}) do
    Patient.changeset(patient, attrs)
  end

  def patient_token_sign_in(email, password) do
    case patient_reg_auth(email, password) do
      {:ok, patient} ->
        Guardian.encode_and_sign(patient)
      _ ->
        {:error, :unauthorized}
    end
  end

  defp patient_reg_auth(reg, password) when is_binary(reg) and is_binary(password) do
    with {:ok, patient} <- get_patient_by_reg(reg),
    do: verify_patient_password(password, patient)
  end

  defp get_patient_by_reg(reg) when is_binary(reg) do
    case Repo.get_by(%Patient{}, reg: reg) do
      nil ->
        dummy_checkpw()
        {:error, "Login error."}
      %Patient{} ->
        {:ok, %Patient{}}
    end
  end

  defp verify_patient_password(password, %Patient{} = patient) when is_binary(password) do
    if checkpw(password, patient.password_hash) do
      {:ok, patient}
    else
      {:error, :invalid_password}
    end
  end

